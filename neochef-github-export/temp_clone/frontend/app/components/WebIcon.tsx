interface WebIconProps {
  name: string;
  size?: number;
  color?: string;
  style?: any;
}

export const WebIcon: React.FC<WebIconProps> = ({ name, size = 24, color = '#000', style }) => {
  // Sur le web, utiliser des emojis
  if (Platform.OS === 'web') {
    const emoji = iconToEmoji[name] || '•';
    return (
      <Text style={[{ fontSize: size * 0.8, color, textAlign: 'center' }, style]}>
        {emoji}
      </Text>
    );
  }
  
  // Sur mobile, utiliser Ionicons normalement
  return <Ionicons name={name as any} size={size} color={color} style={style} />;
};

export default WebIcon;
